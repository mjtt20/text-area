
function textLeft(){
    var grabTheArea = document.getElementById('text-area');
    grabTheArea.style.textAlign = 'left';
}
function textCenter(){
    var grabTheArea = document.getElementById('text-area');
    grabTheArea.style.textAlign = 'center';
}
function textRight(){
    var grabTheArea = document.getElementById('text-area');
    grabTheArea.style.textAlign = 'right';
}
